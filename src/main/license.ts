let cachedLicense: {
  license_key: string;
  licensee: string;
  tier: string;
  expires_at: string;
} | null = null;

export function initLicense() {
  const lic = getLicense();
  if (lic) {
    if (verifyAndCacheLicense(lic.license_key)) {
      return;
    } else {
      // Invalid or expired, clear it
      clearLicense();
    }
  }
  cachedLicense = null;
}

export function getActiveTier(): 'FREE' | 'PRO' {
  return 'PRO';
}

export function activateLicense(licenseKey: string): { success: boolean; message: string } {
  const success = verifyAndCacheLicense(licenseKey);
  if (success && cachedLicense) {
    saveLicense({
      license_key: licenseKey,
      licensee: cachedLicense.licensee,
      tier: cachedLicense.tier,
      expires_at: cachedLicense.expires_at,
    });
    return { success: true, message: `Successfully activated PRO license for ${cachedLicense.licensee}!` };
  }
  return { success: false, message: 'Invalid or expired license key.' };
}

export function deactivateLicense() {
  clearLicense();
  cachedLicense = null;
}

export function verifyAndCacheLicense(licenseKey: string): boolean {
  try {
    const parts = licenseKey.trim().split('.');
    if (parts.length !== 2) return false;

    const [payloadB64, signatureHex] = parts;
    const payload = Buffer.from(payloadB64, 'base64').toString('utf8');
    const licenseData = JSON.parse(payload);

    // Verify properties
    if (!licenseData.email || !licenseData.tier || !licenseData.expires) {
      return false;
    }

    // Expiry check
    if (new Date(licenseData.expires) < new Date()) {
      return false;
    }

    // Cryptographic validation
    const verifier = crypto.createVerify('SHA256');
    verifier.update(payloadB64);
    const isValid = verifier.verify(PUBLIC_KEY, signatureHex, 'hex');

    if (isValid) {
      cachedLicense = {
        license_key: licenseKey,
        licensee: licenseData.email,
        tier: licenseData.tier.toUpperCase(),
        expires_at: licenseData.expires,
      };
      return true;
    }
    return false;
  } catch (err) {
    return false;
  }
}

// Limits Checking
export interface LimitCheckResult {
  allowed: boolean;
  reason?: string;
}

export function checkBatchLimits(filePaths: string[]): LimitCheckResult {
  const tier = getActiveTier();
  if (tier === 'PRO') {
    return { allowed: true };
  }

  // Free limits: Max 2 files
  if (filePaths.length > 2) {
    return {
      allowed: false,
      reason: 'Free tier is limited to a maximum of 2 files per batch. Upgrade to PRO to process unlimited files.'
    };
  }

  // Free limits: Max 10MB per file
  for (const filePath of filePaths) {
    try {
      const stats = fs.statSync(filePath);
      const sizeInMB = stats.size / (1024 * 1024);
      if (sizeInMB > 10) {
        return {
          allowed: false,
          reason: `File size exceeds the 10MB limit for Free tier (${filePath.split('/').pop()}: ${sizeInMB.toFixed(1)}MB). Upgrade to PRO for unlimited file sizes.`
        };
      }
    } catch (err) {
      // Ignore stat failures during pre-check, will be caught during execution
    }
  }

  return { allowed: true };
}

export function checkToolAccess(toolName: string): LimitCheckResult {
  const tier = getActiveTier();
  if (tier === 'PRO') {
    return { allowed: true };
  }

  // Free tier has access to basic operations: Merge, Split, Protect, Unlock, Watermark, PDF to JPG
  const allowedTools = [
    'merge',
    'split',
    'protect',
    'unlock',
    'watermark',
    'pdf2jpg',
    'jpg2pdf',
    'rotate'
  ];

  if (!allowedTools.includes(toolName.toLowerCase())) {
    return {
      allowed: false,
      reason: `The '${toolName}' tool is a premium feature. Please upgrade to PRO to unlock.`
    };
  }

  return { allowed: true };
}
